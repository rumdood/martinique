namespace Martinique.Common.Models
{
    public class RgbColor
    {
        private int red = 0;
        private int green = 0;
        private int blue = 0;

        public int Red
        {
            get
            {
                return red;
            }

            set
            {
                red = value;
            }
        }

        public int Green
        {
            get
            {
                return green;
            }

            set
            {
                green = value;
            }
        }

        public int Blue
        {
            get
            {
                return blue;
            }

            set
            {
                blue = value;
            }
        }
    }
}
